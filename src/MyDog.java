public class MyDog {


    private boolean hungry;
    private boolean thirsty;

    public boolean isHungry() {
        return hungry;
    }
    public void setHungry(boolean hungry) {
        this.hungry = hungry;
    }

    public boolean isThirsty() {
        return thirsty;
    }
    public void setThirsty(boolean thirsty) {
        this.thirsty = thirsty;
    }
}
